          <h2><strong>MTMIS - Online Vehicle Verification and Registration Details</strong></h2>

<p>MTMIS is an <strong>online vehicle verification</strong> system by the Government of Pakistan to find out <strong>Vehicle Registration Details</strong> and verify all the relevant information of vehicles all over Pakistan. The <strong>MTMIS Online</strong> system will help in keeping the records of several aspects of automobile taxation and motor vehicles laws; like integrated computerization of motor vehicle registrations, motor vehicle examinations, issuance of route permits and fitness certifications, issuance of driving licenses, enforcement of traffic rules and regulations, and automation of criminal records.</p>

<p>The project proves helpful on the following grounds:</p>

<ol>
	<li>Help reduce hassle, increase transparency and enable fast communication from the central database.</li>
	<li>Regulating the proper registration of on-road motor vehicles.</li>
	<li>Facilitates better public service delivery.</li>
	<li>Enhancement of revenues.</li>
	<li>Hub for other agencies and users.</li>
</ol>

<h2><strong>Excise and Taxation Online Verification</strong></h2>

<p>In collaboration with <strong>excise and taxation</strong> department MTMIS also functions as <strong>motor registration authority</strong>, the plan was to put the entire vehicle registration data online --accessible to the public.&nbsp;<strong>MTMIS Website</strong> could also help to prevent fraudulent deals and trade of stolen vehicles.</p>

<p>The <strong>MTMIS</strong>&nbsp;<strong>Online Vehicle Verification System</strong> can also help to check if the car has been transferred properly to your name after you have bought the car/bike and also to confirm if the car/bike has been transferred to the new buyers&rsquo; name after selling it in the open market. Considering the law and order situation in our country, it is quite necessary these days to go through this confirmation process to make sure there are no loose ends.</p>

<h3><strong>How to Use MTMIS?</strong></h3>

<p>The system is quite simple to use. You simply enter the registration number of the car you are after, and the system goes through the records with your query. The following information is then displayed when you request the details of a particular <a href="#" title="bike - 2Wheels">car</a> or <a href="home" title="Bikes - 2Wheels">bike</a>.</p>

<ul>
	<li>Registration Date</li>
	<li>Model year</li>
	<li>Engine number</li>
	<li>Owner&rsquo;s name (company or an individual)</li>
	<li>Tax paid till or tax due from</li>
	<li>Vehicle body type</li>
	<li>CPLC Clear or Not (for Sindh)</li>
	<li>Final Remarks</li>
</ul>

<p>The above-mentioned information can be very important especially where there is an issue of unpaid tokens or taxes when buying or selling vehicles. Such unexpected expenditure can increase the overall cost of the vehicle. The tokens and taxes of luxury bike are already quite high. Also, you can check the potential duplicity of a vehicle&rsquo;s number plates, or if the car is not cleared from security agencies, so the details mentioned above are very important</p>

<p>Unfortunately, the conditions of some of the government offices and their dealings (customer relations) with the public are not up to the standard, and whenever possible we try to avoid them unless it is absolutely necessary. The MTMIS initiative is an amazing project in this regard of saving time and money of the public and hassle of going to the Excise and Taxation Department to sort out such important information. The majority of the world has seen the shift from manual databases to online data. It is great to see Pakistan following suit as well.</p>

<p><span style="line-height: 20.8px;"><strong>MTMIS website</strong> or Excise and taxation department for vehicle verification for Baluchistan is unfortunately not live yet but all other provinces have a functioning one.</span></p>

