<h2 dir="ltr"><span id="docs-internal-guid-3016841a-7fff-7fff-191b-519fd954e160">Petrol Quality in Pakistan</span></h2>

<p dir="ltr"><span id="docs-internal-guid-3016841a-7fff-7fff-191b-519fd954e160">For a long time period of time,&nbsp;Pakistan had a supply of low grade petrol in the country while the rest of the world was using finer quality fuel in their automobiles. Even the most undeveloped countries had better quality fuel supplies than Pakistan. It was the year 2016 when Pakistan&nbsp;started to import better quality petrol with a higher Research Octane Number (RON), 92 to be exact. Today, most oil marketing companies in Pakistan refine RON</span>&nbsp;95 or 97 petrol.</p>

<h2 dir="ltr">Hi-Octane Prices</h2>

<p dir="ltr">OGRA does not control Hi-Octane petrol prices in Pakistan. Thus, Hi-Octane prices vary across different Petrol Pumps.</p>

<h2 dir="ltr">Euro Fuel Emission Standards in Pakistan</h2>

<p dir="ltr">Pakistan has officially adopted the Euro V&nbsp;emission standards to improve engine performance of our vehicles.This also&nbsp;helps create a greener environment for Pakistan&#39;s future&nbsp;generations.</p>

<p dir="ltr">Previously, Pakistan adopted the Euro II Emission Standards back in 2012, when Euro II was already&nbsp;outdated.&nbsp;Now, Pakistan has skipped Euro III and Euro IV to adopt Euro V standards in 2020 which was actually introduced in 2009 in other countries. Currently many countries are following Euro VI Emission Standards since 2014, and would switch to Euro VII which is expected around 2025.</p>

