<h2 dir="ltr"><span id="docs-internal-guid-63739afa-7fff-0745-2214-73bd9afb414b">The Frequent Petrol Rate Changes in Pakistan</span></h2>

<p dir="ltr"><span id="docs-internal-guid-63739afa-7fff-0745-2214-73bd9afb414b">The Oil and Gas Regulatory Authority (OGRA) controls the petrol rates in Pakistan. </span>OGRA has a practice of revising all fuel prices twice a month. The new prices are announced on the 15th and the last day of each month and usually remain in effect for the next 15 days. Moreover, petrol prices are different in different areas and regions of Pakistan.&nbsp;</p>

<p dir="ltr"><span id="docs-internal-guid-63739afa-7fff-0745-2214-73bd9afb414b">The petroleum prices in Pakistan are directly related to the </span>global transformations of oil and gas. However, they are also affected by many other local factors such as the supply of petroleum resources in the country, economic conditions, political instability, etc. As a result, the public witnesses frequent ups and downs in the fuel prices.&nbsp;</p>

